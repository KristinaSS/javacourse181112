package com.musala.javacourse181112;

public class ConditionsExample {
    public static void main(String[] args) {
        if (2 > 2) {
            System.out.println("true");
        } else if (5 > 6) {
            System.out.println("0");
        } else if (6 > 7) {
            System.out.println("1");
        } else if (7 == 7) {
            System.out.println("2");
        }
    }

}
